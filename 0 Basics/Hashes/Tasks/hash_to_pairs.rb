def hash_to_pairs(hash)
  pairs = []
  hash.each { |key, val | pairs << [key, val]}
  return pairs
end

print hash_to_pairs({"name"=>"skateboard", "wheels"=>4, "weight"=>"7.5 lbs"}) #=> [["name", "skateboard"], ["wheels", 4], ["weight", "7.5 lbs"]]
puts
